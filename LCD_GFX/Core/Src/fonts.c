// Funções auxiliares
// ============================================================================

const uint8_t* font_get_char_data(const Font* font, char c) {
    if (c < font->first_char || c > font->last_char) {
        c = '?'; // Caractere padrão para não suportados
        if (c < font->first_char || c > font->last_char) {
            return NULL;
        }
    }
    
    uint32_t index = (c - font->first_char);
    uint32_t bytes_per_char = ((font->width * font->height) + 7) / 8;
    
    return &font->data[index * bytes_per_char];
}

uint16_t font_get_char_width(const Font* font, char c) {
    (void)c; // Para fontes monospace, todos os chars têm a mesma largura
    return font->width;
}

uint16_t font_get_char_height(const Font* font) {
    return font->height;
}
